class ShowMemeHandler(webapp2.RequestHandler):
    def post(self):
        results_template = the_jinja_env.get_template('templates/results.html')
        color1 = self.request.get('color1')
        color2 = self.request.get('color2')
        dessert = self.request.get('dessert')
        name = self.request.get('name')

        values = {
            "color1": color1,
            "color2": color2,
            "dessert": dessert,
            "name": name
        }
        self.response.write(results_template.render(values))

